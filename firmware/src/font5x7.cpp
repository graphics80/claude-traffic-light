const uint8_t* glyph5x7(char c) {
    if(c >= 'a' && c <= 'z') c -= 32; // to uppercase

    if(c >= '0' && c <= '9') {
        static const uint8_t* d[10] = {G_0,G_1,G_2,G_3,G_4,G_5,G_6,G_7,G_8,G_9};
        return d[c - '0'];
    }
    if(c >= 'A' && c <= 'Z') {
        static const uint8_t* a[26] = {G_A,G_B,G_C,G_D,G_E,G_F,G_G,G_H,G_I,G_J,
                                       G_K,G_L,G_M,G_N,G_O,G_P,G_Q,G_R,G_S,G_T,
                                       G_U,G_V,G_W,G_X,G_Y,G_Z};
        return a[c - 'A'];
    }
    switch(c) {
        case '%': return G_PCT;
        case '-': return G_DASH;
        case '.': return G_DOT;
        case '_': return G_USCORE;
        case '/': return G_SLASH;
        default:  return G_SPACE;
    }
}
